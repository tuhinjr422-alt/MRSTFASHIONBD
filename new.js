let selectedProduct='';
let currentProducts=[];
let visibleCount=5;

function showProducts(category){

const grid=document.getElementById('productGrid');
const section=document.getElementById('productsSection');
const title=document.getElementById('productTitle');

grid.innerHTML='';

title.innerText=category.toUpperCase()+' Collection';

currentProducts = products[category];
visibleCount = 5;

loadProducts();

section.style.display='block';
section.scrollIntoView({behavior:'smooth'});
}

function loadProducts(){

const grid=document.getElementById('productGrid');

grid.innerHTML='';

currentProducts.slice(0,visibleCount).forEach((product,index)=>{

grid.innerHTML+=`
<div class="product-card">

<img src="${product.images[0]}" class="main-img" id="mainImg${index}">

<div class="thumbs">
<img src="${product.images[0]}" onclick="changeImage(${index},'${product.images[0]}')">
<img src="${product.images[1]}" onclick="changeImage(${index},'${product.images[1]}')">
<img src="${product.images[2]}" onclick="changeImage(${index},'${product.images[2]}')">
</div>

<div class="product-info">
<h4>${product.name}</h4>
<div class="price">${product.price}</div>
<p class="desc">${product.desc}</p>

<button class="buy-btn" onclick="openForm('${product.name}','${product.price}','${product.images[0]}','${product.desc}')">
Order Now
</button>

</div>
</div>
`;

});

if(currentProducts.length > visibleCount){

grid.innerHTML += `
<div style="grid-column:1/-1;text-align:center;margin-top:10px;">
<button onclick="loadMore()" style="padding:14px 30px;border:none;border-radius:12px;background:#111;color:white;font-size:17px;font-weight:bold;cursor:pointer;">
Load More Products
</button>
</div>
`;

}

}

function loadMore(){
visibleCount += 5;
loadProducts();
}

function changeImage(index,img){
document.getElementById('mainImg'+index).src=img;
}

function openForm(name,price,img,desc){

selectedProduct=name+' - '+price;

document.getElementById('formPopup').style.display='flex';

document.getElementById('productPreview').innerHTML=`
<img src="${img}">
<h2>${name}</h2>
<div class="price">${price}</div>
<p>${desc}</p>
`;
}

function closeForm(){
document.getElementById('formPopup').style.display='none';
}


